CREATE TABLE IF NOT EXISTS local_files (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    mdx_id INTEGER,
    name TEXT,
    numberLines INTEGER,
    modified INTEGER DEFAULT CURRENT_TIMESTAMP,
    inletSpeed INTEGER,
    remark TEXT DEFAULT ' '
);

CREATE TABLE IF NOT EXISTS mdx (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT,
    filename TEXT,
    create_time INTEGER DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE IF NOT EXISTS mdx_dat (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    mdx_id INTEGER,
    pos INTEGER,
    value INTEGER
);

CREATE TABLE IF NOT EXISTS settings (
    id INTEGER PRIMARY KEY AUTOINCREMENT,
    name TEXT,
    value NUMERIC
);

